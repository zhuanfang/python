
def test_type():
    # 整型
    int_value = 666
    print(int_value)

    # 浮点型
    float_value = 6.123456
    print(float_value)

    # 字符串
    str_value = 'hello,sister'
    print(str_value)

    # 转移字符串
    str_escape_value = '这是转义的\'hello world!\''
    print(str_escape_value)

    # 布尔值 True、False
    bool_true_value = True
    print(bool_true_value)

    # 空值
    none_value = None
    print(none_value)

    # 常量-变量名 全部大写
    CONS_VALUE = '嘿嘿锅'
    print(CONS_VALUE)